#!/bin/bash
#显示图形化的彩色方块,形状为俄罗斯方块的四种基本形状.
#方法是设计彩色的背景来实现,知道原理的话,还是很简单的.

clear   #清屏
echo -e "\033[43m[][]\n[][]\033[0m"                  #田字型的方块
echo #什么都没有输出,由于echo命令默认在行尾加上换行符,所以这一句相当于换行
echo -e "  \033[46m[]\n[][][]\033[0m"                #土字型的方块
echo
echo -e "\033[42m[][]\n\033[0m  \033[42m[][]\033[0m" #Z字型的方块
echo
echo -e "    \033[45m[]\n[][][]\033[0m"               #7字型的方块
echo
echo -e "\033[41m[][][][]\033[0m"                     #一字型的方块
exit 0;
